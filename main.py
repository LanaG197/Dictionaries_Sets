# Словари
# Создаем словарь my_dict с парами Имя (str) - Год рождения (int)
my_dict = {"Alice": 1990, "Bob": 1985, "Charlie": 1992}

# Выводим на экран словарь my_dict
print("Словарь my_dict:", my_dict)

# Выводим на экран одно значение по существующему ключу
print("Год рождения Alice:", my_dict.get("Alice"))

# Пытаемся получить значение по отсутствующему ключу, используя метод get(), чтобы избежать ошибки
print("Год рождения для отсутствующего ключа:", my_dict.get("David", "Ключ не найден"))

# Добавляем ещё две произвольные пары в словарь my_dict
my_dict["David"] = 1988
my_dict["Eva"] = 1995

# Удаляем одну из пар по существующему ключу и выводим значение этой пары на экран
removed_value = my_dict.pop("Bob", "Ключ не найден")
print("Удаленное значение для ключа 'Bob':", removed_value)

# Выводим на экран обновленный словарь my_dict
print("Обновленный словарь my_dict:", my_dict)

# Множества
# Создаем переменную my_set и присваиваем ей множество
my_set = {1, 2, 2, 3, 4.5, "hello", "world", "hello", (1, 2), (1, 2)}

# Выводим на экран множество my_set (отобразятся только уникальные значения)
print("Initial set:", my_set)

# Добавляем 2 произвольных элемента в множество my_set
my_set.add(10)
my_set.add("new_element")

# Удаляем один любой элемент из множества my_set
my_set.remove(1)  # Удаляем элемент 1

# Выводим на экран измененное множество my_set
print("Modified set:", my_set)
